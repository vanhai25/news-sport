                        <h2>LỊCH SỮ ĐỘI TUYỂN BÓNG ĐÁ QUỐC GIA VIỆT NAM
                        </h2>
                        	
                        <p class="excert">
                           Đội tuyển bóng đá quốc gia Việt Nam là đội tuyển bóng đá nam quốc gia đại diện cho Việt Nam tham dự các giải đấu môn thể thao bóng đá nam quốc tế và được Liên đoàn bóng đá Việt Nam (VFF) quản lý.

Thời điểm Việt Nam bị chia cắt thành hai miền, có hai đội bóng tự nhận là đội tuyển bóng đá quốc gia tồn tại ở mỗi miền và cả hai đều được kiểm soát bởi các Hiệp hội bóng đá Việt Nam tương ứng. Vào cuối những năm 1950, Đội tuyển bóng đá quốc gia Việt Nam Cộng hòa, là một trong bốn đội tiến vào vòng chung kết Cúp bóng đá châu Á 1956 và Cúp bóng đá châu Á 1960, đứng hạng tư cả hai lần. Đội cũng đã vô địch Giải Merdeka lần thứ 10 tại Malaysia năm 1966. Sau khi Việt Nam thống nhất năm 1976, Hiệp hội bóng đá Việt Nam được đổi tên thành Liên đoàn bóng đá Việt Nam.

Do những sự kiện lịch sử xảy ra ở nước này trong suốt thế kỷ 20 (đa phần là chiến tranh), với sự chiếm đóng của Nhật Bản ở Đông Dương, Chiến tranh Đông Dương, Chiến tranh Việt Nam, Chiến tranh biên giới Việt–Trung, Chiến tranh biên giới Tây Nam, xung đột với Thái Lan, bóng đá Việt Nam có thể được coi là vẫn còn rất sơ khai và chưa được biết đến trên toàn cầu. Mặc dù vậy, Việt Nam ngày càng được chú ý vì là một đội bóng có tinh thần mạnh mẽ, với thành tích vị trí thứ 4 vào năm 1956 và 1960 tại Asian Cup với tư cách Việt Nam Cộng hòa; và mặc dù đội chỉ tham gia hai kỳ Asian Cup vào năm 2007 và 2019 với tư cách là một quốc gia thống nhất, đội bóng đã trở thành đội đại diện đến từ Đông Nam Á có thành tích tốt nhất trong cả hai lần, đều lọt vào tứ kết.

Từ những năm 1990 khi Việt Nam gia nhập bóng đá toàn cầu, môn thể thao này sớm trở thành một phần của xã hội Việt Nam và là vũ khí để chống lại danh tiếng tiêu cực của đất nước do chiến tranh Việt Nam và sau đó là xung đột với các nước láng giềng. Điều này làm cho đội tuyển quốc gia trở thành một phần của chủ nghĩa dân tộc Việt Nam. Những người hâm mộ bóng đá Việt Nam được mệnh danh là một trong những người hâm mộ cuồng nhiệt nhất trên thế giới, nổi tiếng với những lễ kỷ niệm lớn về thành tích của đội,[6] bất kể đó là đội tuyển quốc gia hay đội tuyển trẻ.
                        </p>
                        <strong>Thời kỳ đầu (1896-1954)</strong>
                        <p>
                          


Bóng đá Việt Nam thời kỳ đầu với các cầu thủ Việt và Pháp trong giải Championat Cochinchine, k. 1922–23.
Sự ra đời của bóng đá tại Việt Nam bắt đầu từ năm 1896 trong thời thuộc địa Pháp. Ở giai đoạn đầu, môn thể thao này chỉ được chơi giữa các công chức, thương nhân và binh lính Pháp. Người Pháp sau đó khuyến khích người Việt địa phương chơi bóng đá và một số môn thể thao khác được giới thiệu cho họ để chuyển sự quan tâm của họ khỏi chính trị, dẫn đến môn thể thao này được lan truyền sang các khu vực khác, chủ yếu là miền Bắc và miền Trung. Ngày 20 tháng 7 năm 1908, tờ Lục tỉnh Tân văn đưa tin trận cầu giữa hai đội bóng thuần cầu thủ người Việt đã được diễn ra. Đến năm 1928, một số người đứng ra thành lập Tổng cục Thể thao An Nam tại Sài Gòn, cùng trong năm ấy cử một đội bóng Việt Nam sang thi đấu ở Singapore.[8] Nhiều câu lạc bộ bóng đá địa phương sau đó được thành lập ở cả miền Bắc và miền Nam, mặc dù vậy phải đến sau Thế chiến II, các câu lạc bộ bóng đá trong khu vực mới bắt đầu trở nên có tổ chức hơn. Cùng thời gian đó Việt Nam chơi trận đấu quốc tế đầu tiên trước đội tuyển Hàn Quốc ở Sài Gòn, kết quả thua 2-4.
                        </p>
                        <strong>Tại các giải châu lục và thế giới</strong>
                        <p>
                        	

Đội tuyển Việt Nam trước lượt trận thứ hai bảng D Cúp bóng đá châu Á 2019 gặp đội tuyển Iran.
Vòng loại World Cup 1994, do Trần Bình Sự làm huấn luyện viên ở bảng đấu có Triều Tiên, Qatar, Indonesia và Singapore, Việt Nam thắng duy nhất Indonesia loạt trận đầu sau đó thua cả bốn trận lượt về. Lư Đình Tuấn là cầu thủ đầu tiên ghi bàn tại vòng loại World Cup. Việt Nam mất vé Asian Cup 1996 vì thua Hàn Quốc, toàn thua vòng loại World Cup 1998 gặp Tajikistan, Turkmenistan và Trung Quốc, mất vé Asian Cup 2000 vì thua Trung Quốc. − Vòng loại World Cup 2002, đội hòa một, thắng ba trận trước Mông Cổ và Bangladesh và thua Ả Rập Xê Út cả hai lượt đấu, nhìn đội Tây Á đi tiếp.

Năm 2003, Việt Nam cử đội tuyển U–23 dự vòng loại Cúp bóng đá châu Á, thua Oman với tỷ số kỉ lục 0-6, thua Hàn Quốc và thắng Nepal loạt trận đầu. Loạt trận sau thì thắng tối thiểu Hàn Quốc, Nepal rồi thua tiếp 0-2 Oman.

Năm 2004, Edson Tavares trở lại dẫn dắt đội bị loại ở vòng loại World Cup 2006 trước đó có chiến thắng duy nhất trước Maldives nhờ tạm quyền Nguyễn Thành Vinh.

Lần đầu Việt Nam đồng đăng cai và dự giải châu lục là Asian Cup 2007. Tại vòng bảng, đội hạ UAE nhờ hai bàn thắng của Quang Thanh và Công Vinh sau đó hòa Qatar rồi thua ngược Nhật Bản 1-4, lọt vào tứ kết thua Iraq, đội đã vô địch Asian Cup năm đó. Thầy trò Alfred Riedl tái ngộ thua UAE cả hai lượt đi-về vòng loại World Cup 2010 với tổng tỷ số kỷ lục 6-0.

Vòng loại Asian Cup 2011, đội thắng 1, hòa 2 trận khác trước Liban và Syria, còn lại toàn thua trong đó thua Trung Quốc 1-6.

Vòng loại World Cup 2014, dưới quyền của HLV Falko Götz, đội đã hạ Ma Cao cả hai lượt trận, thua Qatar sân khách và thắng đối thủ lượt về. Hoàng Văn Phúc đưa đội dự vòng loại Asian Cup 2015 thua 5 trận, thắng trận thủ tục với Hồng Kông.

Trong chiến dịch vòng loại thứ 2 World Cup 2018, Việt Nam thua Thái Lan 0-1 và thắng Đài Loan 2-1 trên sân khách, sau đó hòa Iraq 1-1 và thua 0-3 trong trận tái đấu Thái Lan tại sân Mỹ Đình. Năm 2016, Nguyễn Hữu Thắng lên thay Toshiya Miura, đội đã thắng Đài Loan 4-1 trên sân nhà Mỹ Đình. Iraq trong trận quyết định đã hạ Việt Nam 1-0 trên sân trung lập để đi tiếp. Kết thúc ở vị trí thứ 3, Việt Nam lọt vào vòng 3 vòng loại Asian Cup 2019 đối đầu Afghanistan, Campuchia, và Jordan. Đội thắng Campuchia cả hai lượt trận, hòa 4 trận còn lại trong thời gian mà Hữu Thắng, Mai Đức Chung và Park Hang-seo lần lượt thay nhau dẫn dắt.

Ở bảng D Asian Cup 2019, Việt Nam khởi đầu bằng trận thua ngược Iraq 2-3 rồi thua tiếp Iran 0-2 và hạ Yemen 2-0 lượt đấu cuối, đi tiếp ở vị trí cuối top 4 đội đứng thứ 3 có thành tích tốt nhất vì hơn Liban ở chỉ số fair-play. Vòng 16 đội, Việt Nam hòa 1-1 trong 120 phút thi đấu và thắng Jordan 4-2 trong loạt sút luân lưu, vào đến tứ kết thua 0-1 trước Nhật Bản. Mặc dù phải về nước sớm, nhưng đội vẫn nhận được rất nhiều sự quan tâm, yêu mến và khen ngợi, điển hình là dành cho thủ môn Đặng Văn Lâm bởi anh đã có những pha cứu thua không tưởng và rất tuyệt vời cho đội tuyển Việt Nam. Anh chỉ phải vào lưới nhặt bóng đúng 1 lần trong trận đấu và đó chỉ là một cú sút phạt đền thành công của Doan Ritsu sau khi Bùi Tiến Dũng phạm lỗi với anh trong khu vực cấm địa. Văn Lâm đã đổ người đúng hướng và thậm chí còn suýt nữa chạm được tay vào bóng, nhưng định mệnh đã gọi tên Nhật Bản vào bán kết. Thậm chí, một số cổ động viên Việt Nam còn nói Văn Lâm "xứng đáng là thủ môn xuất sắc nhất giải đấu" và sự thật anh cũng đã có tên trong danh sách 5 thủ môn xuất sắc nhất Asian Cup 2019. Đội tuyển Việt Nam cũng có thể tự hào về hành trình của họ trên đất UAE khi họ là đội bóng Đông Nam Á đầu tiên vào được đến tứ kết tại Asian Cup và nằm trong danh sách 8 đội mạnh nhất châu Á năm 2019.

